<h3 class="w3-text-grey">Richtige Fußstellung</h3>
<p>Die Füße sollten auf den Pedalen nicht zu weit außen stehen, oder sogar nach außen hin abknicken. Mit den vorderen Fußballen auf den Pedalen stehen.</p>
<h2 class="w3-text-primary">Technik</h2>
<h3 class="w3-text-grey">Ballführen</h3>
<p>In der Regel sollte man den Ball grundsätzlich vor sich führen, nicht neben sich. Abwechselnd Vor- und Rückhandberührungen beim Ballführen.</p>
<h3 class="w3-text-grey">Pässe</h3>
<div class="w3-margin-left">
    <ul class="w3-ul w3-leftbar w3-border-tertiary w3-primary">
    <li>Pässe sollten unbedingt flach gespielt werden: Flache Pässe kann man leicht spielen, indem man beim Passen den Schläger mit Ball in der Kelle aktiv über den Boden zieht (man sollte ihn hören können).</li>
        <li>Pässe in den Fahrweg des passannehmenden Spielers spielen</li>
        <li>Man sollte sowohl Rückhand- wie auch Vorhandpässe verwenden</li>
        <li>Hochschauen zum Spieler, dem man zupasst</li>
        <li>Bälle bleiben bei der Ballannahme flach, wenn man diese mit Gefühl annimmt, die Kelle nicht zu offen hält und den Schläger nicht zu fest hält.</li>
        <li>Spieler sollten ihre Kelle dahin halten, wo sie den Pass auch bekommen wollen.</li>
    </ul>
</div>
<h3 class="w3-text-grey">Schlenzen</h3>
<p>Beim Schlenzen den Oberkörper über den Schläger beugen und mit dem Schläger Druck auf den Boden ausüben. Handgelenke beim Schlenzen mitbenutzen.</p>
<h3 class="w3-text-grey">Schießen</h3>
<p>Mit viel Kraft kurz ausgeholt, bei Linksauslegern beim Ausholen die rechte Schulter vorziehen (linke Schulter bei Rechtsauslegern). Auf den richtigen Treffpunkt des Balles achten (erkennt man am Geräusch). Beim Schießen sollte man "Holzfäller-Schüsse" vermeiden: Also Schüsse mit einer sehr weiten schwungvollen Ausholbewegung und Verdrehung des Einrades.</p>
<h2 class="w3-text-primary">Boxsystem</h2>
<p>Das Boxsystem hat sich inzwischen etabliert und wird von einigen guten Teams gespielt. Es ist wichtig, dass jeder Spieler aufmerksam spielt und sich bewusst ist, wo und wie seine Mitspieler fahren.</p>
