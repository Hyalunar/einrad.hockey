<h3 class="w3-text-grey">Schlägerlänge</h3>
<div class="w3-margin-left">
    <ul class="w3-ul w3-leftbar w3-border-tertiary">
        <li>Auf dem Einrad sitzend: Schläger reicht vom Boden aus etwa zwischen Brustbein und Kinn</li>
        <li>Guter Ansatzpunkt: leicht unter Schulterhöhe (wieder auf dem Einrad sitzend)</li>
    </ul>
</div>
<h3 class="w3-text-grey">Schlägerhaltung</h3>
<div class="w3-margin-left">
    <ul class="w3-ul w3-leftbar w3-border-tertiary">
        <li>Schlägerseite ist unabhängig von Rechts- oder Linkshändigkeit</li>
        <li>Im aktiven Spiel: Nicht auf den Schläger stützen - beide Daumen müssen am Schläger immer nach unten zeigen</li>
        <li>Obere Hand deckt das Schlägerende ab, die untere Hand ist flexibel am Schläger</li>
        <li>Handgelenke sollten beim Spielen aktiv benutzt werden</li>
